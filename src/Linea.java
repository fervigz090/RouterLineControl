// Clase que representa una línea de comunicación
class Linea {
    private boolean sobrecargada;

    public Linea() {
        this.sobrecargada = false;
    }

    public boolean isSobrecargada() {
        return sobrecargada;
    }

    public void setSobrecargada(boolean sobrecargada) {
        this.sobrecargada = sobrecargada;
    }
}
